    private void JBACEPTARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBACEPTARActionPerformed
    String dni = JTXTDNI.getText().trim();
    String nombre = JTXTNOMBRE.getText().trim();
    String email = JTXTEMAIL.getText().trim();
    String telefono = JTXTTELEFONO.getText().trim();
    int monto = Integer.parseInt(JTXTMonto.getText());

    // Validar monto dentro del rango 300 a 1000
    try {
        monto = Integer.parseInt(JTXTMonto.getText().trim());
        if (monto < 300 || monto > 1000) {
            JOptionPane.showMessageDialog(this, "El monto debe estar entre 300 y 1000.");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Solo se permiten números en el monto.");
        return;
    }
    
    FacadeRegistros facade = new FacadeRegistros();
    String resultado = facade.registrarCliente(dni, nombre, email, telefono);
  

    // Registrar el prototipo en el Factory
     JOptionPane.showMessageDialog(this, resultado);

    if (resultado.toLowerCase().contains("registrado")) {
        JBsiguiente.setEnabled(true);
    }
    }//GEN-LAST:event_JBACEPTARActionPerformed

