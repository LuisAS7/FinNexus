    private void JTXTMontoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JTXTMontoActionPerformed
          try {
          int valor = Integer.parseInt(JTXTMonto.getText());
          if (valor >= 300 && valor <= 1000) {
              SliderMonto.setValue(valor);
          } else {
              JOptionPane.showMessageDialog(null, "Ingresa un valor entre 300 y 1000");
              JTXTMonto.setText(String.valueOf(SliderMonto.getValue())); // Restaura el valor anterior
          }
      } catch (NumberFormatException ex) {
          JOptionPane.showMessageDialog(null, "Solo se permiten números");
          JTXTMonto.setText(String.valueOf(SliderMonto.getValue())); 
      }

    }//GEN-LAST:event_JTXTMontoActionPerformed

