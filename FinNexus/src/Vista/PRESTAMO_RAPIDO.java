    private void JBsiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBsiguienteActionPerformed
    
    String dni = JTXTDNI.getText().trim();
    double monto = Double.parseDouble(JTXTMonto.getText().trim());

    // Crear el siguiente paso y pasarle los datos
    PRESTAMO_RAPIDO1 paso2 = new PRESTAMO_RAPIDO1();
    paso2.setDatosCliente(dni, monto);

    // Crear el comando
    ComandoAbrirFrame comando = new ComandoAbrirFrame(this, paso2);

    // Ejecutar el comando desde el controlador
    ControladorPrestamo controlador = new ControladorPrestamo(); // O usa uno ya existente
    controlador.ejecutar(comando);

    // Notificar el paso al Observer
    PrestamoObservable.notificar("Paso2");
    }//GEN-LAST:event_JBsiguienteActionPerformed

 
